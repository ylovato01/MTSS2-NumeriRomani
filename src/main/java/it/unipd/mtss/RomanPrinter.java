package it.unipd.mtss;


public class RomanPrinter 
{
  
  public static String print(int num){
    
    return printAsciiArt(IntegerToRoman.convert(num));
  }
  
  private static String costruisciRiga(String Matrix[][],int riga) {
    String ret="";
    for(int colonna=0;colonna<Matrix[riga].length;colonna++) {
      ret+=Matrix[riga][colonna];
    }
    return ret;
  }
  
  private static String printAsciiArt(String romanNumber)
  {
    String I[][]={{" ","_","_","_","_","_"," "},
            {"|","_"," "," "," ","_","|"},
              {" "," ","|"," ","|"," "," "},
              {" "," ","|"," ","|"," "," "},
              {" ","_","|"," ","|","_"," "},
              {"|","_","_","_","_","_","|"}
             };
    String V[][]={{"_","_"," "," "," "," "," "," ","_","_"},
              {"\\"," ","\\"," "," "," "," ","/"," ","/"},
              {" ","\\"," ","\\"," "," ","/"," ","/"," "},
              {" "," ","\\"," ","\\","/"," ","/"," "," "},
              {" "," "," ","\\"," "," ","/"," "," "," "},
              {" "," "," "," ","\\","/"," "," "," "," "}        
           };
    String X[][]={{"_","_"," "," "," ","_","_"},
              {"\\"," ","\\"," ","/"," ","/"},
              {" ","\\"," ","V"," ","/"," "},
              {" "," ",">"," ","<"," "," "},
              {" ","/"," ","."," ","\\"," "},
              {"/","_","/"," ","\\","_","\\"}
           };
    String L[][]={{" ","_"," "," "," "," "," "," "},
            {"|"," ","|"," "," "," "," "," "},
            {"|"," ","|"," "," "," "," "," "},
            {"|"," ","|"," "," "," "," "," "},
            {"|"," ","|","_","_","_","_"," "},
            {"|","_","_","_","_","_","_","|"}
             };
    String C[][]={{" "," ","_","_","_","_","_"," "},
            {" ","/"," ","_","_","_","_","|"},
            {"|"," ","|"," "," "," "," "," "},
            {"|"," ","|"," "," "," "," "," "},
            {"|"," ","|","_","_","_","_"," "},
            {" ","\\","_","_","_","_","_","|"},
           };
    String D[][]={{" ","_","_","_","_","_"," "," "},
            {"|"," "," ","_","_"," ","\\"," "},
            {"|"," ","|"," "," ","|"," ","|"},
            {"|"," ","|"," "," ","|"," ","|"},
            {"|"," ","|","_","_","|"," ","|"},
            {"|","_","_","_","_","_","/"," "}
           };
    String M[][]={{" ","_","_"," "," ","_","_"," "},
            {"|"," "," ","\\","/"," "," ","|"},
            {"|"," ","\\"," "," ","/"," ","|"},
            {"|"," ","|","\\","/","|"," ","|"},
            {"|"," ","|"," "," ","|"," ","|"},
            {"|","_","|"," "," ","|","_","|"}
           };
    String ret="";
    for(int r=0;r<6;r++) {
      for(int c=0;c<romanNumber.length();c++) {
        switch(romanNumber.charAt(c)) {
          case 'I':
            ret+=costruisciRiga(I,r);
            break;
          case 'V':
            ret+=costruisciRiga(V,r);
            break;
          case 'X':
            ret+=costruisciRiga(X,r);
            break;
          case 'L':
            ret+=costruisciRiga(L,r);
            break;
          case 'C':
            ret+=costruisciRiga(C,r);
            break;
          case 'D':
            ret+=costruisciRiga(D,r);
            break;
          case 'M':
            ret+=costruisciRiga(M,r);
            break;
          default:
              break;
        }
      }
      ret+="\n";
    }  
    return ret;
  }
  
}
